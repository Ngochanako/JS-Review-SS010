let num1: number = 10;
let num2: number = 5;
let num3: number;

// Cộng
num3 = num1 + num2;
console.log("Sum:", num3); // Kết quả: 15

// Trừ
num3 = num1 - num2;
console.log("Difference:", num3); // Kết quả: 5

// Nhân
num3 = num1 * num2;
console.log("Product:", num3); // Kết quả: 50

// Chia
num3 = num1 / num2;
console.log("Quotient:", num3); // Kết quả: 2

// Thay đổi kiểu dữ liệu của biến num1 thành string
num1 = "Hello"; // Lỗi
// Thay đổi kiểu dữ liệu của biến num2 thành boolean
num2 = true; // Lỗi
